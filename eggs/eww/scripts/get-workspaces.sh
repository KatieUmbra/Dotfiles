# Description: eww helper shell script that tracks workspaces

workspaces () {
    niri msg -j workspaces | jq -cM '[.[] | {id: .idx, name, empty: .active_window_id}] | sort_by(.id)'
}

workspaces
niri msg event-stream | while read -r line; do
    workspaces
done
